 package default_packages;

public class PreincrementAndPostincrement {
	
	public static void main(String[] args) {
		
		int a=40;
		--a;//1-40=39
		a--;//39(-1)
		--a;//1-38=37
		a--;//37(-1)
		a--;//36(-1)
		--a;//1-35=34
		a--;//34(-1)
		--a;//1-33=32
		a--;//32(-1)
		
		System.out.println(a);
		
	}

}
